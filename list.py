# slicing in lists allows you to access a range of elements by specifying a start and end index. 
# The syntax is list[start:end], where the start index is inclusive and the end index is exclusive.
# concatenation of strings : + is used to combine two or more strings together.
# print(list1 + list2)
# repetition of strings : * is used to repeat a string multiple times.
# print (list2 * 3)
# append is a method used to add a single element to the end of a list.
# list.append(element)
# append function updates the list but does not return any value (returns None).
# LIST ARE MUTABLE
# insert adds an element at a specific index in the list.
# list.insert(index, element)
# extend adds all elements of an iterable (like another list) to the end of the list.
# list.extend([iterable])
# append only adds one element whereas extend can add multiple elements from another iterable.
# remove method removes the first occurrence of a specified value from the list.
# list.remove(value)
# pop method removes and returns the element at a specified index (default is the last element).
# list.pop(index)
# reverse method reverses the elements of the list in place.
# list.reverse()
# sort method sorts the elements of the list in ascending order by default.
#  You can also specify a custom sorting order using the key parameter.
# list.sort()- will sort the list in ascending order by default.
# list.sort(reverse=True) - will sort the list in descending order.
# count method returns the number of occurrences of a specified value in the list.
# list.count(value)
# in function is used to check if a specified value exists in the list. It returns True if the value is found, otherwise False.
# value in list
# print("dhruvi" in list1) - will return True if "dhruvi" is present in list1, otherwise False.
# not in function is used to check if a specified value does not exist in the list. It returns True if the value is not found, otherwise False.
# value not in list
# print("dhruvi" not in list1) - will return True if "dhruvi" is not present in list1, otherwise False.
# min function is used to find the minimum value in a list of numbers.
# min(list)
# print(min(list1)) - will return the minimum value in list1.
# max function is used to find the maximum value in a list of numbers.
# max(list)
# print(max(list1)) - will return the maximum value in list1.
# sum function is used to calculate the sum of all elements in a list of numbers.
# sum(list)
# print(sum(list1)) - will return the sum of all elements in list1.
# list in a list is a list that contains other lists as its elements.
