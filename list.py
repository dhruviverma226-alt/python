# list is a built-in data structure in Python that is used to store multiple items in a single variable. 
# Lists are ordered, mutable (changeable), and allow duplicate elements. 
# They are defined by enclosing elements in square brackets [].
# example: my_list = [1, 2, 3, "hello", True]
# indexing in lists starts from 0, meaning the first element is at index 0, the second at index 1, and so on.
# slicing in lists allows you to access a range of elements by specifying a start and end index. 
# The syntax is list[start:end], where the start index is inclusive and the end index is exclusive.
